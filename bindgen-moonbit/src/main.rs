use regex::Regex;
use std::collections::HashMap;
use std::fs;

fn c_type_to_moonbit(c_type: &str) -> String {
    let type_map: HashMap<&str, &str> = [
        ("int32_t", "Int"),
        ("uint8_t", "Int"),
        ("float", "Float"),
        ("double", "Double"),
        ("void", "Unit"),
        // C strings map to `Bytes`, not `String`: MoonBit's native `String` is
        // UTF-16 and not NUL-terminated, so passing it where C expects
        // `const char *` truncates at the first embedded NUL. The high-level
        // wrapper is responsible for producing NUL-terminated UTF-8 `Bytes`.
        ("const char *", "Bytes"),
        ("const char*", "Bytes"),
        ("char *", "Bytes"),
        ("char*", "Bytes"),
    ]
    .iter()
    .cloned()
    .collect();

    let trimmed = c_type.trim();
    type_map
        .get(trimmed)
        .map(|s| s.to_string())
        .unwrap_or_else(|| format!("/* {} */", trimmed))
}

#[derive(Debug)]
struct Function {
    return_type: String,
    name: String,
    params: Vec<(String, String)>,
    borrow_params: Vec<String>,
}

fn parse_c_header(content: &str) -> Vec<Function> {
    let func_regex = Regex::new(
        r"(?m)^\s*([a-zA-Z_][a-zA-Z0-9_\s\*]*)\s+([a-zA-Z_][a-zA-Z0-9_]*)\s*\(([^)]*)\)\s*;",
    )
    .unwrap();

    let mut functions = Vec::new();

    for cap in func_regex.captures_iter(content) {
        let return_type = cap[1].trim().to_string();
        let name = cap[2].to_string();
        let params_str = cap[3].trim();

        let params = if params_str == "void" || params_str.is_empty() {
            Vec::new()
        } else {
            params_str
                .split(',')
                .map(|p| {
                    let p = p.trim();
                    
                    // Handle pointer types specially
                    if p.contains('*') {
                        // Find the last * and split after it
                        if let Some(star_pos) = p.rfind('*') {
                            let type_part = &p[..=star_pos];
                            let name_part = p[star_pos + 1..].trim();
                            (type_part.trim().to_string(), name_part.to_string())
                        } else {
                            (p.to_string(), String::new())
                        }
                    } else {
                        // Regular type, split on last whitespace
                        let parts: Vec<&str> = p.rsplitn(2, char::is_whitespace).collect();
                        if parts.len() == 2 {
                            (parts[1].trim().to_string(), parts[0].to_string())
                        } else {
                            (p.to_string(), String::new())
                        }
                    }
                })
                .collect()
        };

        // Auto-detect borrow params: heap-backed types (String/Bytes) should be
        // borrowed so the FFI call doesn't take ownership of the pointer.
        let borrow_params: Vec<String> = params
            .iter()
            .filter(|(type_str, _)| {
                let moonbit_type = c_type_to_moonbit(type_str);
                moonbit_type == "String" || moonbit_type == "Bytes"
            })
            .map(|(_, name)| name.clone())
            .collect();

        functions.push(Function {
            return_type,
            name,
            params,
            borrow_params,
        });
    }

    functions
}

fn generate_moonbit_ffi(functions: &[Function]) -> String {
    let mut output = String::new();

    output.push_str("// Auto-generated by bindgen-moonbit\n");
    output.push_str("// Do not edit manually\n\n");

    for func in functions {
        let moonbit_return = c_type_to_moonbit(&func.return_type);

        let moonbit_params: Vec<String> = func
            .params
            .iter()
            .map(|(type_str, name)| {
                let moonbit_type = c_type_to_moonbit(type_str);
                format!("{} : {}", name, moonbit_type)
            })
            .collect();

        let params_str = if moonbit_params.is_empty() {
            "()".to_string()
        } else {
            format!("({})", moonbit_params.join(", "))
        };

        let return_str = format!(" -> {}", moonbit_return);

        // Add #borrow attribute if needed
        if !func.borrow_params.is_empty() {
            output.push_str(&format!("#borrow({})\n", func.borrow_params.join(", ")));
        }

        output.push_str(&format!(
            "extern \"C\" fn {}_ffi{}{} = \"{}\"\n\n",
            func.name, params_str, return_str, func.name
        ));
    }

    output
}

fn main() {
    let args: Vec<String> = std::env::args().collect();

    if args.len() < 2 {
        eprintln!("Usage: bindgen-moonbit <input.h> [output.mbt]");
        std::process::exit(1);
    }

    let input_file = &args[1];
    let output_file = args.get(2).map(|s| s.as_str()).unwrap_or("bindings.mbt");

    let content = match fs::read_to_string(input_file) {
        Ok(c) => c,
        Err(e) => {
            eprintln!("Error reading {}: {}", input_file, e);
            std::process::exit(1);
        }
    };

    let functions = parse_c_header(&content);

    if functions.is_empty() {
        eprintln!("No functions found in {}", input_file);
        std::process::exit(1);
    }

    println!("Found {} functions:", functions.len());
    for func in &functions {
        println!("  - {}", func.name);
    }

    let moonbit_code = generate_moonbit_ffi(&functions);

    match fs::write(output_file, moonbit_code) {
        Ok(_) => println!("\nGenerated: {}", output_file),
        Err(e) => {
            eprintln!("Error writing {}: {}", output_file, e);
            std::process::exit(1);
        }
    }
}
